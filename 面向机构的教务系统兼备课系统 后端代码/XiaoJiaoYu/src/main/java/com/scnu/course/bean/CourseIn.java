package com.scnu.course.bean;

public class CourseIn {
	private int date;
	
	private String startTime;
	
	private String endTime;
	
	private String student;
	
	private String subject;
	
	private String grade;
	
	private String status;
	
	private int residCourse;
	
	private String courseTopic;
	
	private int isMadePlan;
	
	private int stime;
	
	private int etime;
	
	private String gradeName;

	public int getDate() {
		return date;
	}

	public void setDate(int date) {
		this.date = date;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getStudent() {
		return student;
	}

	public void setStudent(String student) {
		this.student = student;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public int getResidCourse() {
		return residCourse;
	}

	public void setResidCourse(int residCourse) {
		this.residCourse = residCourse;
	}

	public String getCourseTopic() {
		return courseTopic;
	}

	public void setCourseTopic(String courseTopic) {
		this.courseTopic = courseTopic;
	}

	public int getIsMadePlan() {
		return isMadePlan;
	}

	public void setIsMadePlan(int isMadePlan) {
		this.isMadePlan = isMadePlan;
	}

	public int getStime() {
		return stime;
	}

	public void setStime(int stime) {
		this.stime = stime;
	}

	public int getEtime() {
		return etime;
	}

	public void setEtime(int etime) {
		this.etime = etime;
	}

	public String getGradeName() {
		return gradeName;
	}

	public void setGradeName(String gradeName) {
		this.gradeName = gradeName;
	}
	
}
